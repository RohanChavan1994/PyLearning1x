def sum1(a1, b1):
    return a1 + b1


def dif1(a1, b1):
    return a1 - b1


def mul1(a1, b1):
    return a1 * b1


def div1(a1, b1):
    return a1 / b1


a = float(input("Enter number 1: "))
b = float(input("Enter number 2: "))

print(f"Sum of {a} and {b} is:", sum1(a, b))
print(f"Difference of {a} and {b} is:", dif1(a, b))
print(f"Product of {a} and {b} is:", mul1(a, b))
print(f"Division of {a} and {b} is:", div1(a, b))
